    private void fillComboBox(int index) {
        
        cbSearch.removeAllItems();
        ArrayList<FutureOperation> futureOperations = new ArrayList<>();
        
        for (int i = 0; i < SystemFunctions.getOperationList().size(); i++) {
            
            InstantOperation opp = (InstantOperation) SystemFunctions.getOperationList().get(i);
            
            if (opp instanceof FutureOperation && opp.getOperationType().equalsIgnoreCase("Claim") && opp.getProduct().getPrice() > 0)
                futureOperations.add((FutureOperation)opp);
        }
        
        for (int i = 0; i < futureOperations.size(); i++) {
            cbSearch.addItem(String.valueOf(futureOperations.get(i).getProduct().getBarcode()));
        }
        try {
            cbSearch.setSelectedIndex(index);
        } catch (Exception e) {
            cbSearch.setSelectedIndex(-1);
        }
        
    }
    
    private void fillTheTable() {
        int index = 0;
        
        
        DefaultTableModel model = (DefaultTableModel)tClaim.getModel();
        
        //at beginning there will be no row at all.
        model.setRowCount(0);
        
        ArrayList<FutureOperation> futureOperations = new ArrayList<>();
        
        for (int i = 0; i < SystemFunctions.getOperationList().size(); i++) {
            
            InstantOperation opp = (InstantOperation) SystemFunctions.getOperationList().get(i);
            
            if (opp instanceof FutureOperation && opp.getOperationType().equalsIgnoreCase("Claim"))
                futureOperations.add((FutureOperation)opp);
        }
        
        while (futureOperations.size() > index ) {
           model.addRow(new Object[]{
               futureOperations.get(index).getDeadline(),
               futureOperations.get(index).getProduct().getName(),
               futureOperations.get(index).getProduct().getBarcode(), 
               futureOperations.get(index).getProduct().getStock(),
               futureOperations.get(index).getProduct().getPrice(), 
               futureOperations.get(index).getValueAddedTax(),
               futureOperations.get(index).getDiscount(),
               futureOperations.get(index).getInterestRate()}); 
           index++;
        }
       
       
    }
    
