    private void btnPayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPayActionPerformed
        
 
        for (int i = 0; i < SystemFunctions.getOperationList().size(); i++) {
           
                if(SystemFunctions.getOperationList().get(i) instanceof FutureOperation){
                    if (    SystemFunctions.getOperationList().get(i).getOperationType().equalsIgnoreCase("Claim") &&
                            ((FutureOperation)(SystemFunctions.getOperationList().get(i))).getProduct().getBarcode() == Integer.parseInt(cbSearch.getSelectedItem().toString()) && 
                            ((FutureOperation)(SystemFunctions.getOperationList().get(i))).getProduct().getPrice() >= Double.parseDouble(tfPaying.getText())) {
                        
                       SystemFunctions.getOperationList().get(i).getProduct().setPrice(SystemFunctions.getOperationList().get(i).getProduct().getPrice() - Double.parseDouble(tfPaying.getText()));
                       SystemFunctions.saveOperationsToDatabase();
                       SystemFunctions.getCompany().setClaim(SystemFunctions.getCompany().getClaim() - Double.parseDouble(tfPaying.getText()));
                       SystemFunctions.getCompany().setBalance(SystemFunctions.getCompany().getBalance() + Double.parseDouble(tfPaying.getText()));
                       lblMessagePaying.setText("The Process is successful");
                       taClaim.setText(null);
                       fillComboBox(cbSearch.getSelectedIndex());
                       fillTheTable();
                       return;
                    }
                    else{
                       lblMessagePaying.setText("The Process is fail");

                    }
                       
                }
        }
        
        
        
        
    }//GEN-LAST:event_btnPayActionPerformed

    private void cbSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbSearchActionPerformed
        // TODO add your handling code here:
        if(cbSearch.getSelectedIndex()>=0){
        for(int i =0; i<SystemFunctions.getOperationList().size();i++){    
            String result = "Claim Information\n\n";
            if(SystemFunctions.getOperationList().get(i) instanceof FutureOperation){
                 if (
                         SystemFunctions.getOperationList().get(i).getOperationType().equalsIgnoreCase("Claim") &&
                         ((FutureOperation)(SystemFunctions.getOperationList().get(i))).getProduct().getBarcode() == Integer.parseInt(cbSearch.getSelectedItem().toString())){
                        
                        result += "Deadline: " + ((FutureOperation)(SystemFunctions.getOperationList().get(i))).getDeadline() + "\n";
                        result += "Product Name: " + ((FutureOperation)(SystemFunctions.getOperationList().get(i))).getProduct().getName() + "\n";
                        result += "Barcode No:" + ((FutureOperation)(SystemFunctions.getOperationList().get(i))).getProduct().getBarcode() + "\n"; 
                        result += "Amount: " + ((FutureOperation)(SystemFunctions.getOperationList().get(i))).getProduct().getStock() + "\n";
                        result += "Price: " + ((FutureOperation)(SystemFunctions.getOperationList().get(i))).getProduct().getPrice() + "\n"; 
                        result += "VAT: " + ((FutureOperation)(SystemFunctions.getOperationList().get(i))).getValueAddedTax() + "\n";
                        result += "Discount: " + ((FutureOperation)(SystemFunctions.getOperationList().get(i))).getDiscount() + "\n";
                        result += "Interest Rate: " + ((FutureOperation)(SystemFunctions.getOperationList().get(i))).getInterestRate() + "\n";
                        taClaim.setText(result);
                 }
            }   }
        }
    }//GEN-LAST:event_cbSearchActionPerformed

