    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            if (SystemFunctions.getTimeDifference(tfDeadline.getText()) > 0) {
                futureOperation = new FutureOperation("Claim", new Product(Integer.parseInt(tfBarcode.getText()), tfName.getText(), Integer.parseInt(tfAmount.getText()), Double.parseDouble(tfPrice.getText())), Double.parseDouble(tfVAT.getText()), Double.parseDouble(tfDiscount.getText()), tfDeadline.getText(), Double.parseDouble(tfInterestRate.getText()));
            
                if (SystemFunctions.isOperationAvailable(futureOperation)) {
                    SystemFunctions.addOperation(futureOperation);
                    fillComboBox(0);
                    fillTheTable();
                    lblMessageClaim.setText("The products are sold.");
                } else {
                    lblMessageClaim.setText("There is no stock");
                }
                
            }
            else{
                lblMessageClaim.setText("The process is fail");
            }
        } catch (ParseException ex) {
            lblMessageClaim.setText("Enter the date correctly!!");
        } catch (NumberFormatException e) {
            lblMessageClaim.setText("Fill the blanks correctly!!");
        } 
    }//GEN-LAST:event_btnAddActionPerformed

